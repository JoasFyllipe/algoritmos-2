package entities;

public class Calculadora {
    public double num1;
    public double num2;

    public double somaNum(){
        return num1+num2;
    }
    public double subtrairNum(){
        return num1-num2;
    }
    public double multiplicarNum(){
        return num1*num2;
    }
    public double divisaoNum(){
        return num1/num2;
    }

}
